package com.solie.firebaseexample;

public class list {
    String name;
    String thing;

    public list() {

    }

    public list(String name, String thing) {
        this.name = name;
        this.thing = thing;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getThing() {
        return thing;
    }

    public void setThing(String thing) {
        this.thing = thing;
    }
}
